export const Clock = () => {
    const ref = useRef<SVGSVGElement>(null);

    const { time, animate } = useTal();

    useEffect(() => {
        if (!ref.current) {
            return;
        }

        const seconds = time.getSeconds();
        let minutes = time.getMinutes();
        let hours = time.getHours();
        hours = hours > 12 ? hours - 12 : hours;

        minutes = minutes * 60 + seconds;
        hours = hours * 3600 + minutes;

        const hour = ref.current.getElementById('hour');
        const minute = ref.current.getElementById('minute');
        const second = ref.current.getElementById('second');

        (hour as SVGLineElement).setAttribute('transform', `rotate(${360 * (hours / 43200)} ,31,31)`);
        (minute as SVGLineElement).setAttribute('transform', `rotate(${360 * (minutes / 3600)} ,31,31)`);
        (second as SVGLineElement).setAttribute('transform', `rotate(${360 * (seconds / 60)} ,31,31)`);
    }, [time]);

    if (!time || !animate) {
        return null;
    }

    return (
        <svg
            ref={ref}
            className="clockFace"
            x={220}
            y={390}
        >
            <ClockFace />
        </svg>
    );
};
